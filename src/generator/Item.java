package generator;

public class Item {

   float p;
   float w;
public Item(float p, float w) {
	super();
	this.p = p;
	this.w = w;
}
public float getP() {
	return p;
}
public void setP(float p) {
	this.p = p;
}
public float getW() {
	return w;
}
public void setW(float w) {
	this.w = w;
}
   
		
}
